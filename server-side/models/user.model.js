import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
    isAdmin:{
        type: Boolean,
        default: false,
    },
    name: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email:{
        type: String,
        required: true,
        unique: true,
    },
    password:{
        type: String,
        required: true,
    },      
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
    profilePicture:{
        type: String,
        default:"https://images.unsplash.com/photo-1511367461989-f85a21fda167?fm=jpg&q=60&w=3000&ixlib=rb-4.1.0&ixid=M3wxMjA3fDB8MHxzZWFyY2h8M3x8cHJvZmlsZXxlbnwwfHwwfHx8MA%3D%3D"
    },
    profileImage: {
        data: Buffer,
        contentType: String
    }
    
}, { timestamps: true });

const User = mongoose.model('User', userSchema);

export default User;